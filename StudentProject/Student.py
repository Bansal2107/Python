class Student:
    courses=[]

    def getName(self):
        return self.name

    def setName(self,name):
        self.name=name

    def setRollNo(self,rollNo):
        self.rollNo=rollNo

    def getRollNo(self):
        return self.rollNo

    def getCourses(self):
        return self.courses

    def setCourses(self,courses):
        self.courses=courses